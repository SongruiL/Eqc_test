//! 表达式构造器模块
//!
//! 按运算符类型分类的 Expr 构造方法。
//!
//! ## 分类
//!
//! - **arithmetic**: 基础算术运算（加减乘除、幂、根号等）
//! - **trigonometric**: 三角函数和双曲函数
//! - **exponential**: 指数和对数函数
//! - **special**: Gamma、Beta、误差函数等特殊函数
//! - **bessel**: Bessel、Hankel、Struve 函数系列
//! - **polynomial**: Legendre、Hermite、Chebyshev 等正交多项式
//! - **elliptic**: 椭圆积分和 Jacobi 椭圆函数
//! - **distribution**: 概率分布函数
//! - **gsl**: GSL 扩展函数

